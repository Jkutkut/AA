package dam.pruebas.operadores;

public class PruebasOpUnit {

	public static void main(String[] args) {
		int m = 2;
		int n = 5;
		System.out.println("m = " + m + " y n = " + n);
		
		m++; // m=3
		System.out.println("m++ --> " + m);
		
		n--; // n=4
		System.out.println("n-- --> " + n);


	}

}
