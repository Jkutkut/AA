#define LAST_SQRT 46340

int	ft_sqrt(int nb)
{
	int	r;

	if (nb < 0)
		return (0);
	r = 1;
	while (r <= LAST_SQRT && r * r < nb)
		r++;
	if (nb == r * r)
		return (r);
	return (0);
}
