#include <unistd.h>

#define MIN_INT -2147483648

void	ft_putnbr(int nb)
{
	if (nb == MIN_INT)
	{
		write(1, "-2", 2);
		nb = (MIN_INT % 1000000000) * -1;
	}
	else if (nb < 0)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	nb = (nb % 10) + 48;
	write (1, &nb, 1);
}
